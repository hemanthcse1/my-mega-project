package com.hemanth.problemsolving.linkedlist;

public class RemoveDuplicateElementsFromSortedList {

    public class ListNode {
        int data;
        ListNode next;

        ListNode() {
        }

        ListNode(int val) {
            this.data = data;
        }

        ListNode(int data, ListNode next) {
            this.data = data;
            this.next = next;
        }
    }



}
